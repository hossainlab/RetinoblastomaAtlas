def plot_umap(
    adata: sc.AnnData,
    color_keys: list[str],
    title: str,
    out_path: Path,
    use_rep: str = "X_umap",
) -> None:
    """Publication-ready UMAP coloured by multiple keys."""
    ncols = len(color_keys)
    fig, axes = plt.subplots(1, ncols, figsize=(6 * ncols, 5))
    if ncols == 1:
        axes = [axes]
    for ax, key in zip(axes, color_keys):
        sc.pl.umap(adata, color=key, ax=ax, show=False, frameon=False,
                   size=2, alpha=0.7)
        ax.set_title(key, fontsize=11)
    plt.suptitle(title, fontsize=12, fontweight="bold", y=1.02)
    plt.tight_layout()
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info(f"  Saved: {out_path.name}")


def plot_training_history(history: dict, out_path: Path) -> None:
    """Plot scVI training loss curve.

    SIGNIFICANCE:
    Convergence of the ELBO (Evidence Lower BOund) loss confirms that the
    variational autoencoder has reached a stable minimum.  A still-decreasing
    loss at max_epochs would indicate under-training; a loss that diverges
    indicates numerical instability (usually fixed by reducing learning rate).
    """
    train_loss = history.get("train_loss_epoch", [])
    val_loss   = history.get("elbo_validation",  [])
    fig, ax = plt.subplots(figsize=(8, 5))
    if train_loss:
        ax.plot(train_loss, label="Training ELBO", color="#4C9BE8")
    if val_loss:
        ax.plot(val_loss,   label="Validation ELBO", color="#E84C4C",
                linestyle="--")
    ax.set_xlabel("Epoch", fontsize=11)
    ax.set_ylabel("ELBO loss", fontsize=11)
    ax.set_title("scVI training history\n(lower = better)", fontsize=11)
    ax.legend(fontsize=10)
    plt.tight_layout()
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info(f"  Saved: {out_path.name}")


