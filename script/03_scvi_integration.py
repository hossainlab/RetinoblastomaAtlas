def run_integration(
    n_latent: int  = 30,
    n_layers: int  = 2,
    n_hidden: int  = 128,
    max_epochs: int = 400,
    lr: float      = 1e-3,
    leiden_resolutions: list = (0.3, 0.5, 1.0),
) -> sc.AnnData:
    """scVI integration pipeline.

    Steps
    -----
    1. Load normalized atlas.
    2. Visualise pre-integration UMAP (batch effect diagnostic).
    3. Set up scVI model on raw counts with batch covariate.
    4. Train scVI (VAE with negative binomial decoder).
    5. Extract latent representation and compute batch-corrected expression.
    6. Build neighbour graph on latent embedding.
    7. UMAP and Leiden clustering.
    8. Evaluate integration quality (scib-metrics).
    9. Save integrated atlas and trained model.
    """

    # ---- 1. Load --------------------------------------------------------
    log.info("=" * 60)
    log.info("STEP 1 — Loading normalized atlas")
    log.info("=" * 60)
    adata = sc.read_h5ad(IN_H5AD)
    log.info(f"  {adata.n_obs:,} cells × {adata.n_vars:,} genes")

    # ---- 2. Pre-integration UMAP (diagnostic) ---------------------------
    log.info("\nSTEP 2 — Pre-integration UMAP (batch effect diagnosis)")
    log.info(
        "  WHY: Computing UMAP on the un-corrected PCA space shows how\n"
        "  severe the batch effect is. Cells that cluster by dataset\n"
        "  instead of cell type confirm the need for batch correction."
    )
    sc.pp.neighbors(adata, use_rep="X_pca_pre_scvi", n_neighbors=30, n_pcs=30)
    sc.tl.umap(adata, min_dist=0.3)
    adata.obsm["X_umap_pre_scvi"] = adata.obsm["X_umap"].copy()
    plot_umap(
        adata, ["dataset", "sample_id"],
        "UMAP before scVI integration",
        FIG_DIR / "umap_before_integration.pdf",
    )

    # ---- 3. scVI model setup --------------------------------------------
    log.info("\nSTEP 3 — Setting up scVI model")
    log.info(
        f"  Architecture: {n_layers} encoder/decoder layers, "
        f"{n_hidden} hidden units, {n_latent}-d latent space\n"
        "  Gene likelihood: Negative Binomial (accounts for overdispersion\n"
        "  of scRNA-seq counts; superior to Poisson or Gaussian)\n"
        "  Batch key: 'dataset' (GSE168434 vs. GSE249995)\n"
        "  Categorical covariate: 'sample_id' (individual patient)\n"
        "  Continuous covariate: 'pct_counts_mt' (residual technical signal)"
    )
    # scVI requires raw integer counts
    scvi.model.SCVI.setup_anndata(
        adata,
        layer="counts",
        batch_key="dataset",
        categorical_covariate_keys=["sample_id"],
        continuous_covariate_keys=["pct_counts_mt"],
    )
    model = scvi.model.SCVI(
        adata,
        n_latent=n_latent,
        n_layers=n_layers,
        n_hidden=n_hidden,
        gene_likelihood="nb",         # negative binomial decoder
        dispersion="gene-batch",      # per-gene, per-batch dispersion estimate
        use_layer_norm="both",
        use_batch_norm="none",
    )
    log.info(f"  Model summary:\n{model}")

    # ---- 4. Train scVI --------------------------------------------------
    log.info(f"\nSTEP 4 — Training scVI (max_epochs={max_epochs})")
    log.info(
        "  The ELBO (Evidence Lower BOund) is maximised during training.\n"
        "  Early stopping monitors validation ELBO with patience=20 epochs\n"
        "  to prevent overfitting."
    )
    plan_kwargs = {"lr": lr, "weight_decay": 1e-6}
    model.train(
        max_epochs=max_epochs,
        early_stopping=True,
        early_stopping_patience=20,
        early_stopping_monitor="elbo_validation",
        plan_kwargs=plan_kwargs,
        check_val_every_n_epoch=5,
    )
    plot_training_history(model.history, FIG_DIR / "scvi_training_history.pdf")

    # Save trained model weights for reproducibility
    MODEL_DIR.mkdir(parents=True, exist_ok=True)
    model.save(str(MODEL_DIR), overwrite=True)
    log.info(f"  Model saved → {MODEL_DIR.name}/")

    # ---- 5. Extract latent representation -------------------------------
    log.info("\nSTEP 5 — Extracting scVI latent embedding")
    log.info(
        "  The 30-dimensional latent space encodes biological cell identity\n"
        "  with batch effects modelled out.  This embedding is used for:\n"
        "    • Neighbour graph construction (KNN in latent space)\n"
        "    • UMAP visualization\n"
        "    • Leiden clustering"
    )
    adata.obsm["X_scVI"] = model.get_latent_representation()

    log.info("\nSTEP 5b — Computing batch-corrected normalized expression")
    log.info(
        "  scVI also produces a batch-corrected expression matrix by\n"
        "  decoding each cell through a 'reference batch' condition.\n"
        "  Stored in .layers['scvi_normalized'] for downstream differential\n"
        "  expression and pathway activity scoring."
    )
    adata.layers["scvi_normalized"] = model.get_normalized_expression(
        library_size=1e4
    )

    # ---- 6. Neighbour graph on latent space -----------------------------
    log.info("\nSTEP 6 — Building KNN graph on scVI latent space")
    log.info(
        "  n_neighbors=30: balances local and global structure. Larger\n"
        "  values produce smoother UMAP at the cost of local resolution."
    )
    sc.pp.neighbors(adata, use_rep="X_scVI", n_neighbors=30)

    # ---- 7. UMAP + Leiden clustering ------------------------------------
    log.info("\nSTEP 7 — UMAP embedding and Leiden clustering")
    sc.tl.umap(adata, min_dist=0.3)

    log.info(
        f"  Running Leiden clustering at resolutions: {leiden_resolutions}\n"
        "  WHY MULTIPLE RESOLUTIONS: Low resolution gives broad cell-type\n"
        "  clusters (useful for annotation); high resolution gives fine\n"
        "  subclusters within cone precursors (used for Aim 2 and Aim 3)."
    )
    for res in leiden_resolutions:
        sc.tl.leiden(adata, resolution=res, key_added=f"leiden_{res:.1f}")
        n_clusters = adata.obs[f"leiden_{res:.1f}"].nunique()
        log.info(f"  Resolution {res}: {n_clusters} clusters")

    plot_umap(
        adata, ["dataset", "sample_id"],
        "UMAP after scVI integration — batch labels",
        FIG_DIR / "umap_after_integration_dataset.pdf",
    )
    plot_umap(
        adata, ["leiden_0.5", "leiden_1.0"],
        "UMAP after scVI integration — Leiden clusters",
        FIG_DIR / "umap_after_integration_clusters.pdf",
    )

    # ---- 8. Integration quality (scib-metrics) --------------------------
    log.info("\nSTEP 8 — Evaluating integration quality (scib-metrics)")
    log.info(
        "  Computing batch correction vs biological conservation scores.\n"
        "  Target: overall score > 0.7 (Luecken et al. 2022)"
    )
    try:
        import scib
        metrics = scib.metrics.metrics(
            adata,
            adata_int=adata,
            batch_key="dataset",
            label_key="leiden_0.5",
            embed="X_scVI",
            cluster_key="leiden_0.5",
            organism="human",
            verbose=False,
        )
        metrics_df = pd.DataFrame(metrics).T
        metrics_df.to_csv(TAB_DIR / "scib_integration_metrics.csv")
        log.info(f"  scib metrics saved → {TAB_DIR / 'scib_integration_metrics.csv'}")
        log.info(f"  Overall score: {metrics_df.get('Overall Score', 'N/A')}")
    except ImportError:
        log.warning(
            "  scib not installed — skipping integration metrics.\n"
            "  Install with: pip install scib-metrics"
        )

    # ---- 9. Save --------------------------------------------------------
    log.info(f"\nSTEP 9 — Saving integrated atlas → {OUT_H5AD.name}")
    log.info(
        "  Contents:\n"
        "    .X                   : log-normalized expression\n"
        "    .layers['counts']    : raw UMI counts (for scVI re-training)\n"
        "    .layers['lognorm']   : log-normalized counts\n"
        "    .layers['scvi_norm'] : scVI batch-corrected expression\n"
        "    .obsm['X_scVI']      : 30-d scVI latent space\n"
        "    .obsm['X_umap']      : 2-d UMAP coordinates\n"
        "    .obs['leiden_*']     : Leiden cluster labels"
    )
    adata.write_h5ad(OUT_H5AD, compression="gzip")
    log.info("  Done.\n")
    return adata


