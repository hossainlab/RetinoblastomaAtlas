def mad_threshold(series: pd.Series, n_mads: float = 5.0) -> tuple[float, float]:
    """Compute symmetric MAD-based outlier thresholds.

    Using the Median Absolute Deviation (MAD) rather than mean ± n*SD is
    robust to the heavy tails typical of scRNA-seq count distributions.
    A cell is considered an outlier if its metric is more than `n_mads`
    MADs from the sample median.

    Parameters
    ----------
    series : per-cell QC values for ONE sample
    n_mads : number of MADs defining the outlier band (default 5)

    Returns
    -------
    (lower_bound, upper_bound)
    """
    median = np.median(series)
    mad    = np.median(np.abs(series - median))
    return median - n_mads * mad, median + n_mads * mad


def run_scrublet(
    adata_sample: sc.AnnData,
    sample_id: str,
    expected_doublet_rate: float = 0.06,
    threshold: float = 0.25,
) -> pd.Series:
    """Run Scrublet on a single sample and return per-cell doublet scores.

    WHY PER-SAMPLE?
    Scrublet simulates doublets by combining pairs of observed cells.
    If run on the merged atlas, the expected doublet neighbourhood is
    dominated by the most abundant cell types and the per-sample expected
    doublet rate (which depends on loading density) cannot be set correctly.
    Running per-sample avoids both issues.

    Parameters
    ----------
    adata_sample : AnnData for a single sample (raw counts)
    sample_id    : label used in log messages
    expected_doublet_rate : 10x Chromium v3 ≈ 0.8% per 1000 cells loaded.
                            GEMs loaded at ~5000 cells ≈ 4–6% doublets.
    threshold    : Scrublet score above which a cell is called a doublet

    Returns
    -------
    pd.Series (index = cell barcode) of doublet scores
    """
    log.info(f"  Scrublet: {sample_id} — {adata_sample.n_obs:,} cells")
    counts = adata_sample.X
    if issparse(counts):
        counts = counts.toarray()

    scrub = scr.Scrublet(counts, expected_doublet_rate=expected_doublet_rate)
    doublet_scores, predicted_doublets = scrub.scrub_doublets(
        min_counts=2, min_cells=3, n_prin_comps=30, verbose=False
    )
    # If automatic threshold is very low, override with manual threshold
    if scrub.threshold_ < 0.15:
        log.warning(
            f"  Scrublet threshold {scrub.threshold_:.3f} very low for "
            f"{sample_id}; using manual threshold {threshold}"
        )
        predicted_doublets = doublet_scores >= threshold

    n_doublets = predicted_doublets.sum()
    log.info(
        f"  {sample_id}: {n_doublets} doublets ({100*n_doublets/len(doublet_scores):.1f}%)"
    )
    return pd.Series(doublet_scores, index=adata_sample.obs_names, name="doublet_score"), \
           pd.Series(predicted_doublets, index=adata_sample.obs_names, name="is_doublet")


def plot_qc_violins(adata: sc.AnnData, out_path: Path) -> None:
    """Per-sample violin plots for nGenes, nCounts, mt%.

    SIGNIFICANCE:
    Visualising QC distributions per sample reveals whether batch effects
    are driven by technical differences in sequencing depth or cell viability
    rather than biological variation — critical for interpreting the
    subsequent scVI correction.
    """
    fig, axes = plt.subplots(3, 1, figsize=(14, 12))
    metrics = [
        ("n_genes_by_counts", "Genes detected per cell"),
        ("total_counts",      "Total UMI counts per cell"),
        ("pct_counts_mt",     "Mitochondrial gene fraction (%)"),
    ]
    for ax, (metric, title) in zip(axes, metrics):
        sc.pl.violin(
            adata, metric, groupby="sample_id",
            rotation=45, show=False, ax=ax, size=0.3,
        )
        ax.set_title(title, fontsize=11, fontweight="bold")
        ax.set_xlabel("")
    plt.tight_layout()
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info(f"  Saved: {out_path.name}")


def plot_mt_scatter(adata: sc.AnnData, out_path: Path) -> None:
    """Scatter plot: total counts vs mt% coloured by sample.

    SIGNIFICANCE:
    The classic 'hockey stick' pattern (high mt% at low counts) confirms
    that dying cells are being correctly identified.  Cells diverging
    from this pattern at high counts are suspicious for doublets or highly
    metabolically active cell types (e.g., activated macrophages) and
    inform threshold refinement.
    """
    fig, ax = plt.subplots(figsize=(8, 6))
    samples  = adata.obs["sample_id"].cat.categories
    palette  = plt.cm.tab20(np.linspace(0, 1, len(samples)))
    for sample, color in zip(samples, palette):
        mask = adata.obs["sample_id"] == sample
        ax.scatter(
            adata.obs.loc[mask, "total_counts"],
            adata.obs.loc[mask, "pct_counts_mt"],
            s=1, alpha=0.3, color=color, label=sample, rasterized=True,
        )
    ax.set_xlabel("Total UMI counts (log scale)", fontsize=11)
    ax.set_ylabel("Mitochondrial %", fontsize=11)
    ax.set_xscale("log")
    ax.legend(bbox_to_anchor=(1.02, 1), loc="upper left", markerscale=5,
              fontsize=7, frameon=False)
    ax.set_title(
        "QC scatter: total counts vs mitochondrial fraction\n"
        "(high mt% at low counts = dying cells)",
        fontsize=10,
    )
    plt.tight_layout()
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info(f"  Saved: {out_path.name}")


def plot_doublet_histogram(adata: sc.AnnData, out_path: Path) -> None:
    """Histogram of Scrublet scores coloured by predicted doublet status.

    SIGNIFICANCE:
    A well-calibrated Scrublet run produces a bimodal distribution: a
    singlet peak near 0 and a doublet peak at higher scores.  Absence of
    bimodality may indicate that the expected doublet rate parameter
    needs adjustment.
    """
    fig, ax = plt.subplots(figsize=(8, 5))
    doublets  = adata.obs["doublet_score"][adata.obs["is_doublet"]  == True]
    singlets  = adata.obs["doublet_score"][adata.obs["is_doublet"] == False]
    ax.hist(singlets, bins=100, alpha=0.7, color="#4C9BE8", label="Singlet")
    ax.hist(doublets, bins=100, alpha=0.7, color="#E84C4C", label="Doublet")
    ax.set_xlabel("Scrublet doublet score", fontsize=11)
    ax.set_ylabel("Number of cells", fontsize=11)
    ax.set_title("Doublet score distribution (Scrublet)", fontsize=11)
    ax.legend(fontsize=10)
    plt.tight_layout()
    fig.savefig(out_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info(f"  Saved: {out_path.name}")


