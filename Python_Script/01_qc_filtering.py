def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Quality control and cell filtering for RetinoblastomaAtlas",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument("--mt-threshold",      type=float, default=20.0,
                   help="Maximum mitochondrial %% allowed per cell")
    p.add_argument("--min-genes",         type=int,   default=300,
                   help="Hard lower bound on genes detected per cell")
    p.add_argument("--min-counts",        type=int,   default=500,
                   help="Hard lower bound on UMI counts per cell")
    p.add_argument("--n-mads",            type=float, default=5.0,
                   help="Number of MADs for per-sample outlier detection")
    p.add_argument("--doublet-threshold", type=float, default=0.25,
                   help="Scrublet score threshold above which a cell is a doublet")
    return p.parse_args()


if __name__ == "__main__":
    args = parse_args()
    FIG_DIR.mkdir(parents=True, exist_ok=True)
    TAB_DIR.mkdir(parents=True, exist_ok=True)

    run_qc(
        mt_threshold      = args.mt_threshold,
        min_genes         = args.min_genes,
        min_counts        = args.min_counts,
        n_mads            = args.n_mads,
        doublet_threshold = args.doublet_threshold,
    )
