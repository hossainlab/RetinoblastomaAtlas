def run_qc(
    mt_threshold: float = 20.0,
    min_genes: int = 300,
    min_counts: int = 500,
    n_mads: float = 5.0,
    doublet_threshold: float = 0.25,
) -> sc.AnnData:
    """Full QC pipeline.

    Steps
    -----
    1. Load merged raw atlas.
    2. Annotate mitochondrial genes.
       WHY: Mitochondrial transcripts (gene names starting with 'MT-') are
       encoded in the mitochondrial genome. Their fraction is the primary
       proxy for cell viability — dead cells lose cytoplasmic mRNA faster
       than mitochondrial mRNA, so the MT fraction rises as a cell dies.
    3. Compute per-cell QC metrics (Scanpy).
    4. Run Scrublet per sample to score doublets.
    5. Apply MAD-based per-sample filters on nGenes and nCounts.
    6. Apply a global mt% threshold (20% is permissive enough for activated
       immune cells while capturing obvious dead cells).
    7. Remove predicted doublets.
    8. Save QC statistics and filtered AnnData.
    """
    # ---- 1. Load --------------------------------------------------------
    log.info("=" * 60)
    log.info("STEP 1 — Loading merged raw atlas")
    log.info("=" * 60)
    adata = sc.read_h5ad(IN_H5AD)
    log.info(f"  Loaded: {adata.n_obs:,} cells × {adata.n_vars:,} genes")
    log.info(f"  Samples: {adata.obs['sample_id'].nunique()}")
    log.info(f"  Datasets: {adata.obs['dataset'].value_counts().to_dict()}")

    # ---- 2. Annotate mitochondrial genes --------------------------------
    log.info("\nSTEP 2 — Annotating mitochondrial genes")
    adata.var["mt"] = adata.var_names.str.startswith("MT-")
    n_mt = adata.var["mt"].sum()
    log.info(f"  Found {n_mt} mitochondrial genes")

    # ---- 3. QC metrics --------------------------------------------------
    log.info("\nSTEP 3 — Computing per-cell QC metrics")
    sc.pp.calculate_qc_metrics(
        adata,
        qc_vars=["mt"],
        percent_top=None,
        log1p=False,
        inplace=True,
    )
    # Save raw QC table before filtering
    qc_df = adata.obs[
        ["sample_id", "dataset", "n_genes_by_counts", "total_counts", "pct_counts_mt"]
    ].copy()
    qc_df.to_csv(TAB_DIR / "qc_metrics_per_cell_raw.csv")
    log.info(
        f"  Median genes/cell: {adata.obs['n_genes_by_counts'].median():.0f}\n"
        f"  Median counts/cell: {adata.obs['total_counts'].median():.0f}\n"
        f"  Median mt%: {adata.obs['pct_counts_mt'].median():.1f}%"
    )

    # ---- Plot before filtering ------------------------------------------
    plot_qc_violins(adata, FIG_DIR / "qc_violin_before_filter.pdf")
    plot_mt_scatter(adata, FIG_DIR / "qc_scatter_mt_vs_counts.pdf")

    # ---- 4. Doublet detection per sample --------------------------------
    log.info("\nSTEP 4 — Doublet detection (Scrublet, per sample)")
    log.info(
        "  WHY PER-SAMPLE: Scrublet simulates doublets by pairing cells;\n"
        "  running on the full merged atlas mixes doublet rates across\n"
        "  samples, which were loaded at different densities."
    )
    all_scores  = []
    all_predict = []
    for sid in adata.obs["sample_id"].cat.categories:
        mask = adata.obs["sample_id"] == sid
        scores, predicted = run_scrublet(
            adata[mask].copy(), sid, threshold=doublet_threshold
        )
        all_scores.append(scores)
        all_predict.append(predicted)

    adata.obs["doublet_score"] = pd.concat(all_scores).reindex(adata.obs_names)
    adata.obs["is_doublet"]    = pd.concat(all_predict).reindex(adata.obs_names)

    n_doublets = adata.obs["is_doublet"].sum()
    log.info(
        f"  Total predicted doublets: {n_doublets:,} "
        f"({100*n_doublets/adata.n_obs:.1f}% of all cells)"
    )

    plot_doublet_histogram(adata, FIG_DIR / "doublet_scores_histogram.pdf")

    # ---- 5. Per-sample MAD filtering ------------------------------------
    log.info("\nSTEP 5 — Per-sample MAD-based outlier filtering")
    log.info(
        f"  Using {n_mads} MADs; hard lower bounds: "
        f"min_genes={min_genes}, min_counts={min_counts}"
    )
    keep_mask = pd.Series(True, index=adata.obs_names)
    summary_rows = []

    for sid in adata.obs["sample_id"].cat.categories:
        mask = adata.obs["sample_id"] == sid
        sub  = adata.obs.loc[mask]

        lo_g, hi_g = mad_threshold(sub["n_genes_by_counts"], n_mads)
        lo_c, hi_c = mad_threshold(sub["total_counts"],      n_mads)

        lo_g = max(lo_g, min_genes)
        lo_c = max(lo_c, min_counts)

        bad = (
            (sub["n_genes_by_counts"] < lo_g) |
            (sub["n_genes_by_counts"] > hi_g) |
            (sub["total_counts"]      < lo_c) |
            (sub["total_counts"]      > hi_c)
        )
        keep_mask.loc[sub.index[bad]] = False
        summary_rows.append({
            "sample_id": sid,
            "n_cells_before": mask.sum(),
            "lo_genes": lo_g, "hi_genes": hi_g,
            "lo_counts": lo_c, "hi_counts": hi_c,
            "cells_failed_count_filter": int(bad.sum()),
        })
        log.info(
            f"  {sid}: genes [{lo_g:.0f}, {hi_g:.0f}], "
            f"counts [{lo_c:.0f}, {hi_c:.0f}] → removed {bad.sum()} cells"
        )

    # ---- 6. Global mt% filter -------------------------------------------
    log.info(f"\nSTEP 6 — Global mitochondrial filter: mt% < {mt_threshold}%")
    high_mt = adata.obs["pct_counts_mt"] >= mt_threshold
    log.info(f"  Cells with mt% ≥ {mt_threshold}: {high_mt.sum():,}")
    keep_mask = keep_mask & (~high_mt)

    # ---- 7. Remove doublets ---------------------------------------------
    log.info("\nSTEP 7 — Removing predicted doublets")
    keep_mask = keep_mask & (~adata.obs["is_doublet"].astype(bool))

    # ---- Apply filters --------------------------------------------------
    adata_filt = adata[keep_mask].copy()
    n_removed  = adata.n_obs - adata_filt.n_obs
    log.info(
        f"\n{'=' * 60}\n"
        f"  Cells before QC : {adata.n_obs:>8,}\n"
        f"  Cells removed   : {n_removed:>8,} ({100*n_removed/adata.n_obs:.1f}%)\n"
        f"  Cells retained  : {adata_filt.n_obs:>8,}\n"
        f"{'=' * 60}"
    )

    # ---- Remove genes expressed in <20 cells ----------------------------
    log.info("\nSTEP 7b — Removing lowly expressed genes")
    sc.pp.filter_genes(adata_filt, min_cells=20)
    log.info(f"  Genes retained: {adata_filt.n_vars:,}")

    # ---- Preserve raw counts layer for scVI ----------------------------
    log.info("\nSTEP 8 — Storing raw counts in .layers['counts']")
    log.info(
        "  WHY: scVI (script 03) requires integer raw counts as input.\n"
        "  We store them here before any normalisation so they survive\n"
        "  downstream .X modifications."
    )
    adata_filt.layers["counts"] = adata_filt.X.copy()

    # ---- Post-filter QC violin -----------------------------------------
    plot_qc_violins(adata_filt, FIG_DIR / "qc_violin_after_filter.pdf")

    # ---- Summary table --------------------------------------------------
    summary_df = pd.DataFrame(summary_rows)
    summary_df.to_csv(TAB_DIR / "qc_summary_per_sample.csv", index=False)
    log.info(f"\n  Saved summary table: {TAB_DIR / 'qc_summary_per_sample.csv'}")

    # ---- Save ----------------------------------------------------------
    log.info(f"\nSTEP 9 — Saving filtered AnnData → {OUT_H5AD.name}")
    adata_filt.write_h5ad(OUT_H5AD, compression="gzip")
    log.info("  Done.")

    return adata_filt


